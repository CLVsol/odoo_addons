from osv import fields, osv
import tools

class oehealth_comm_area(osv.osv):
    def name_get(self, cr, uid, ids, context=None):
        if context is None:
            context = {}
        if not ids:
            return []
        reads = self.read(cr, uid, ids, ['name','parent_id'], context=context)
        res = []
        for record in reads:
            name = record['name']
            if record['parent_id']:
                name = record['parent_id'][1]+' / '+name
            res.append((record['id'], name))
        return res

    def _area_name_get_fnc(self, cr, uid, ids, prop, unknow_none, context=None):
        res = self.name_get(cr, uid, ids, context=context)
        return dict(res)

    _name = "oehealth.comm_area"
    _columns = {
        'name': fields.char('Community Area Name', size=64, required=True),
        'complete_name': fields.function(_area_name_get_fnc, type="char", string='Name'),
        'parent_id': fields.many2one('oehealth.comm_area', 'Parent Area', select=True),
        'child_ids': fields.one2many('oehealth.comm_area', 'parent_id', 'Child Areas'),
        'note': fields.text('Note'),
    }

    def _check_recursion(self, cr, uid, ids, context=None):
        if context is None:
            context = {}
        level = 100
        while len(ids):
            cr.execute('select distinct parent_id from oehealth_comm_area where id IN %s',(tuple(ids),))
            ids = filter(None, map(lambda x:x[0], cr.fetchall()))
            if not level:
                return False
            level -= 1
        return True

    _constraints = [
        (_check_recursion, 'Error! You cannot create recursive community areas.', ['parent_id'])
        ]
